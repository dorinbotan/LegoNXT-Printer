   public void Print()
   {
      motor.LoadPaper();

      for (int i = 0; i < image.length; i++)
      {
         motor.ReturnCarriage();
         motor.Right(50);

         if (CheckLine(image[i]))
            for (int j = 0; j < image[0].length; j++)
            {
               if (image[i][j])
               {
                  LCD.setPixel(j, i, 1);

                  motor.PenDown();
                  motor.PenUp();
                  motor.Stop();
               }

               motor.Right(6);

               // Show how far we got.
               LCD.clear(1, 1, 3);
               LCD.drawString((int) (i * 100 / image.length) + "%", 1, 1);
            }

         motor.Down(7);
      }

      motor.UnloadPaper();
      motor.ReturnCarriage();
   }

